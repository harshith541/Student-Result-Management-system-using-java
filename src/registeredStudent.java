    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
                                                                                 
    int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student to delete");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this student?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        // rollno is first column
        Object rollno = jTable1.getValueAt(row, 0);

        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/src?useSSL=false&serverTimezone=UTC",
            "root",
            "Harshi123"
        );

// delete result first
PreparedStatement ps2 = con.prepareStatement(
    "DELETE FROM student WHERE rollno = ?"
);
ps2.setObject(1, rollno);
int studentDeleted = ps2.executeUpdate();

PreparedStatement ps1 = con.prepareStatement(
    "DELETE FROM result WHERE rollno = ?"
);
ps1.setObject(1, rollno);
ps1.executeUpdate();

if (studentDeleted > 0) {
    JOptionPane.showMessageDialog(this, "Student deleted successfully");
    ((DefaultTableModel) jTable1.getModel()).removeRow(row);
} else {
    JOptionPane.showMessageDialog(this, "Student not found");
}

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }


    }//GEN-LAST:event_jButton6ActionPerformed

