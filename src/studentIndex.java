    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:; // ✅ MUST exist
                                        
    try {
        // ✅ Correct database name
        Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/src?useSSL=false&serverTimezone=UTC",
            "root",
            "Harshi123"
        );

        String rollno = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword()).trim();

        if (rollno.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter roll number and password");
            return;
        }

        // ✅ Correct SQL query (matches your database)
        String sql = "SELECT * FROM student WHERE rollno = ? AND password = ?";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, rollno);
        pst.setString(2, password);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Login Successful");
            setVisible(false);
            new studentHome(rollno).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Roll Number or Password");
        }

        rs.close();
        pst.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

