    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

    String course = jComboBox1.getSelectedItem().toString();
String branch = jComboBox2.getSelectedItem().toString();
String rollno = jTextField1.getText().trim();
String name = jTextField2.getText().trim();
String gender = jComboBox3.getSelectedItem().toString();
String fathername = jTextField3.getText().trim();
String email = jTextField4.getText().trim();        // ✅
String password = new String(jPasswordField2.getPassword()); // ✅

if (rollno.isEmpty() || email.isEmpty() || password.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Roll number, email and password are required");
    return;
}

try {
    Connection con = DriverManager.getConnection(
        "jdbc:mysql://localhost:3306/src",
        "root",
        "Harshi123"
    );

    String sql =
        "INSERT INTO student (rollno, course, branch, name, gender, fathername, email, password) " +
        "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

    PreparedStatement pst = con.prepareStatement(sql);

    pst.setString(1, rollno);
    pst.setString(2, course);
    pst.setString(3, branch);
    pst.setString(4, name);
    pst.setString(5, gender);
    pst.setString(6, fathername);
    pst.setString(7, email);
    pst.setString(8, password);

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Student added successfully");

    pst.close();
    con.close();

} catch (SQLIntegrityConstraintViolationException e) {
    JOptionPane.showMessageDialog(this, "Roll number or Email already exists!");
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.getMessage());
}



    }//GEN-LAST:event_jButton6ActionPerformed

