    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText().trim();
    String phy = jTextField2.getText().trim();
    String che = jTextField3.getText().trim();
    String math = jTextField4.getText().trim();
    String electrical = jTextField5.getText().trim();
    String electronic = jTextField6.getText().trim();
    try {
    int p = Integer.parseInt(phy);
    int c = Integer.parseInt(che);
    int m = Integer.parseInt(math);
    int e1 = Integer.parseInt(electrical);
    int e2 = Integer.parseInt(electronic);

    if (p < 0 || p > 100 ||
    c < 0 || c > 100 ||
    m < 0 || m > 100 ||
    e1 < 0 || e1 > 100 ||
    e2 < 0 || e2 > 100) {

    JOptionPane.showMessageDialog(this,
            "Marks must be between 0 and 100!",
            "Invalid Marks",
            JOptionPane.ERROR_MESSAGE);
    return;
}


} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this,
            "Please enter valid numeric marks only!",
            "Invalid Input",
            JOptionPane.ERROR_MESSAGE);
    return;
}


    if (rollno.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Roll number required!");
        return;
    }

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");

        Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/src?useSSL=false&serverTimezone=UTC",
            "root",
            "Harshi123"
        );

        // Check student exists
        PreparedStatement ps1 =
            con.prepareStatement("SELECT rollno FROM student WHERE rollno=?");
        ps1.setString(1, rollno);

        ResultSet rs = ps1.executeQuery();

        if (!rs.next()) {
            JOptionPane.showMessageDialog(this, "Roll number not found in student table!");
            con.close();
            return;
        }

        // Insert result
        PreparedStatement ps2 = con.prepareStatement(
            "INSERT INTO result (rollno, phy, che, math, elevtrical, electronic) " +
            "VALUES (?, ?, ?, ?, ?, ?)"
        );

        ps2.setString(1, rollno);
        ps2.setString(2, phy);
        ps2.setString(3, che);
        ps2.setString(4, math);
        ps2.setString(5, electrical);
        ps2.setString(6, electronic);

        ps2.executeUpdate();

        JOptionPane.showMessageDialog(this, "Result saved successfully!");

        con.close();

    } catch (SQLIntegrityConstraintViolationException e) {
        JOptionPane.showMessageDialog(this, "Result already exists for this roll number!");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "ERROR: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton6ActionPerformed

